import turtle as t
t.setup(650,350,400,250)
t.penup()
t.fd(-250)
t.pendown()
t.seth(-40)
t.pencolor("purple")
t.pensize(25)
for i in range(4):
    t.circle(40,80)
    t.circle(-40,80)
t.circle(40,40)
t.fd(40)
t.circle(16,180)
t.fd(40)
t.circle(-40,40)
for i in range(4):
    t.circle(40,80)
    t.circle(-40,80)
t.done()